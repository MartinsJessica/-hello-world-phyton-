primeiro_numero = float(input("Número:"))
segundo_numero = float(input("Número:"))
terceiro_numero = float(input("Número:"))

maior_numero = max(primeiro_numero, segundo_numero, terceiro_numero)
menor_numero = min(primeiro_numero, segundo_numero, terceiro_numero)

print("Maior número:", maior_numero)
print("Menor número:", menor_numero)
